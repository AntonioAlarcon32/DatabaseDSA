package dsa.grupo2.models;

public class Item {
    private String Id;
    private String name;

    public Item() {
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
